    private void btnSendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendActionPerformed
       try{
            String host="redhat.com";
            int port=80;
            int timeOutInMilliSec=5000;// 5 Seconds
            Socket socket = new Socket();
            socket.connect(new InetSocketAddress(host, port), timeOutInMilliSec);
            System.out.println("Internet is Available");
        }
        catch(Exception ex){
           JOptionPane.showMessageDialog(null,"No Internet Connection is Available!","Error!",JOptionPane.ERROR_MESSAGE);
           return;
        }
        sub=txtSubject.getText();
        msg="Competition Pro Support\n"+txtMatter.getText();
        to="sainerahul98@gmail.com";
        from=compMail;
        password=compPassword;
        Properties props = new Properties();    
          props.put("mail.smtp.host", "smtp.gmail.com");    
          props.put("mail.smtp.socketFactory.port", "465");    
          props.put("mail.smtp.socketFactory.class",    
                    "javax.net.ssl.SSLSocketFactory");    
          props.put("mail.smtp.auth", "true");    
          props.put("mail.smtp.port", "465");    
          //get Session   
          Session session = Session.getDefaultInstance(props,    
           new javax.mail.Authenticator() {    
           protected PasswordAuthentication getPasswordAuthentication() {    
           return new PasswordAuthentication(from,password);  
           }    
          });    
          //compose message    
          try {    
           MimeMessage message = new MimeMessage(session);    
           message.addRecipient(Message.RecipientType.TO,new InternetAddress(to));    
           message.setSubject(sub);    
           message.setText(msg);    
           //send message  
           Transport.send(message);    
           JOptionPane.showMessageDialog(null,"Mail sent!\nWe will contact you asap!â˜º", "Support@competition_pro", JOptionPane.INFORMATION_MESSAGE);
           System.out.println("message sent successfully");    
          } catch (MessagingException e) {
              e.printStackTrace();
              JOptionPane.showMessageDialog(null,"Error occoured while contacting with team\nTry again later.!", "Support@competition_pro", JOptionPane.ERROR_MESSAGE);
           
          }    
             
        
    }//GEN-LAST:event_btnSendActionPerformed

